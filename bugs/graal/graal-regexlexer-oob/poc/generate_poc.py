#!/usr/bin/env python3
"""Generate PoC for graal-regexlexer-oob.

Verbatim 91-byte UTF-8 byte sequence from upstream issue #986. Drives
new RegExp(pattern, 'v') which trips StringIndexOutOfBoundsException
in RegexLexer.consumeChar (RegexLexer.java:553) — graaljs treats it
as InternalError instead of converting to JS SyntaxError.
"""

def create_poc(output='poc.bin'):
    blob = bytes([
        0x2f, 0xd6, 0x9c, 0xc4, 0xa3, 0xc3, 0xa0, 0x5c, 0x76,
        0x22, 0x22, 0x22, 0x22, 0x22, 0x22, 0x22, 0x22,
        0xd6, 0x9c, 0xc4, 0xa3, 0xc3, 0xa0,
        0xc0, 0x80, 0xc0, 0x80, 0xc0, 0x80, 0xc0, 0x80,
        0xc0, 0x80, 0xc0, 0x80, 0xc0, 0x80,
        0x6f, 0x22, 0x22, 0x22, 0x22, 0x22, 0x5c, 0x76,
        0xee, 0xb3, 0xb6, 0x76, 0x5a, 0x5c, 0x5c, 0x22, 0x22, 0x22,
        0x5c, 0x76, 0xee, 0xb3, 0xb6, 0x76, 0x5b, 0x5c, 0x5c, 0x22, 0x22,
        0x5c, 0x76, 0xee, 0xb3, 0xb6, 0x76, 0x5b, 0x5c, 0x5c, 0x5c,
        0x76, 0xee, 0xb3, 0xb6, 0x76, 0x5b, 0x5c, 0x5c, 0x5c,
        0x76, 0x76, 0x76, 0x78, 0x2d, 0x5c,
    ])
    with open(output, 'wb') as f:
        f.write(blob)
    print(f"wrote {output} ({len(blob)} bytes)")


if __name__ == '__main__':
    create_poc()
